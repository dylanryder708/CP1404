try:
    numerator = int(input("Enter the numerator: "))
    denominator = int(input("Enter the denominator: "))
    while denominator == 0:
        denominator = int(input("Enter the denominator: "))
    fraction = numerator / denominator
    print(fraction)
except ValueError:
    print("Numerator and denominator must be valid numbers!")
except ZeroDivisionError:
    print("Cannot divide by zero!")
print("Finished.")

# 1. The value error will occur when anything other than an integer is inputted

# 2. The zero division error will occur when 0 is inputted as the denominator

# 3. While loop which keeps asking for denominator if inputted denominator is 0
